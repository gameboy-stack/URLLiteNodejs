const inputBox = document.getElementById("urlinput");

inputBox.addEventListener("change", () => {
    document.getElementById("shorturl").disabled = true;
    var x = document.getElementById("urlinput").value;
    var y = x.replace("/\s+/g","")
//     <!-- ^((ftp|http|https):\/\/)?(www.)?(?!.*(ftp|http|https|www.))[a-zA-Z0-9_-]+(\.[a-zA-Z]+)+((\/)[\w#]+)*(\/\w+\?[a-zA-Z0-9_]+=\w+(&[a-zA-Z0-9_]+=\w+)*)?$ -->
    var pattern = new RegExp(/^(?:(?:(?:https?|ftp):)?\/\/)(?:\S+(?::\S*)?@)?(?:(?!(?:10|127)(?:\.\d{1,3}){3})(?!(?:169\.254|192\.168)(?:\.\d{1,3}){2})(?!172\.(?:1[6-9]|2\d|3[0-1])(?:\.\d{1,3}){2})(?:[1-9]\d?|1\d\d|2[01]\d|22[0-3])(?:\.(?:1?\d{1,2}|2[0-4]\d|25[0-5])){2}(?:\.(?:[1-9]\d?|1\d\d|2[0-4]\d|25[0-4]))|(?:(?:[a-z\u00a1-\uffff0-9]-*)*[a-z\u00a1-\uffff0-9]+)(?:\.(?:[a-z\u00a1-\uffff0-9]-*)*[a-z\u00a1-\uffff0-9]+)*(?:\.(?:[a-z\u00a1-\uffff]{2,})))(?::\d{2,5})?(?:[/?#]\S*)?$/i);
    var res = x.match(pattern);
    
    if(y.length < 1 || !res == true){
            document.getElementById("urlinput").style.borderColor = "#ed4956"; 
            return false;
    }
    else{
            document.getElementById("urlinput").style.borderColor = "#18db18";
            document.getElementById("shorturl").disabled = false;
            return false;
    }
});

